#include "libft.h"

int	ft_find_next_prime(int nb)
{
	int	i;

	i = nb;
	if (nb <= 2)
		i = 2;
	while (i)
	{
		if (ft_isprime(i) == 1)
			return (i);
		i++;
	}
	return (0);
}
