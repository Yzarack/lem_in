int	ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 1;
	if (nb == 0)
		return (0);
	while (sqrt * sqrt < nb)
		sqrt++;
	if (nb % sqrt == 0)
		return (sqrt);
	return (0);
}
