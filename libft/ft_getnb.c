#include "libft.h"

char	*ft_getnb(char *dest, int n_len, int is_neg, int nb)
{
	while (n_len >= is_neg)
	{
		dest[n_len] = (nb % 10) + '0';
		nb = nb / 10;
		n_len--;
	}
	return (dest);
}
